def coding(direction, input_text, shift_number):

    output_text = ""

    for char in input_text:
        if char in alphabet:
            b = alphabet.index(char)
            letter = alphabet[b + shift_number]
            output_text += letter
        else:
            output_text += char

    print(f'Here is the {direction}d result: {output_text}')

